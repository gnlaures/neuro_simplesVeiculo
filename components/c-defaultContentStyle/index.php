    <h1>IBGE prevê safra recorde de 261,7 milhões de toneladas em 2022</h1>
    <h3>Aumento da previsão de agosto é de 3,3% em relação a 2021</h3>
    <br>
    <figure>
        <a href="https://placehold.co/1240x500/eee/ccc" data-fancybox="article">
            <img src="https://placehold.co/1240x500/eee/ccc" alt="Legenda da Imagem">
