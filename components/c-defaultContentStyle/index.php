    <br>
    <p><a href="#">A safra brasileira de cereais</a>, leguminosas e <strong>oleaginosas</strong> deve <b>bater</b> o recorde de 261,7 milhões de toneladas em 2022. Em relação ao ano passado, o aumento <i>previsto</i> é de 3,3% ou 8,5 milhões de toneladas. <u>Porém</u>, a estimativa de agosto ficou 0,7% abaixo do apurado em julho, ou 1,8 milhão de toneladas a menos.</p>
    <br>
    <blockquote>Os dados são do Levantamento Sistemático da Produção Agrícola (LSPA), divulgado nesta quinta-feira, dia 8, pelo Instituto Brasileiro de Geografia e Estatística (IBGE). De acordo com o gerente da pesquisa, Carlos Barradas, a redução na estimativa de julho para agosto ocorreu devido à influência de questões climáticas. <cite>John Doe - CTO</cite></blockquote>
    <br>
    <p>“As principais variações negativas ocorreram no Paraná (-865.300 t), em Goiás (-559.010 t), em Minas Gerais (-532.786 t), no Ceará (-70.185 t), em Alagoas (-24.753 t), no Espírito Santo (-30 t) e no Piauí (-10 t). Mas vale ressaltar que a área colhida alcançou 73 milhões de hectares, 6,5% maior (mais 4,5 milhões de hectares) que a área colhida em 2021, e 0,1% maior (mais 61,1 mil hectares) que no mês anterior. Esses números mostram que os produtores têm investido no aumento da produção da safra devido aos preços elevados das commodities agrícolas”.</p>
